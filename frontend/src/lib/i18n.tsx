type Messages = Record<keyof typeof messages, string>;

type I18nContextValue = {
  t: Messages;
  locale: "en" | "zh";
  setLocale: (locale: "en" | "zh") => void;
};

const I18nCtx = createContext<I18nContextValue>({
  t: messages,
  locale: "en",
  setLocale: () => {},
});

const LANG_KEY = "qa-lang";

function getInitialLocale(): "en" | "zh" {
  try {
    const stored = localStorage.getItem(LANG_KEY);
    if (stored === "zh") return "zh";
  } catch {}
  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<"en" | "zh">(getInitialLocale);

  const setLocale = useCallback((next: "en" | "zh") => {
    setLocaleState(next);
    try { localStorage.setItem(LANG_KEY, next); } catch {}
    document.documentElement.lang = next === "zh" ? "zh-CN" : "en";
  }, []);

  const value: I18nContextValue = {
    t: locale === "zh" ? zhMessages : messages,
    locale,
    setLocale,
  };

  return (
    <I18nCtx.Provider value={value}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useI18n(): I18nContextValue {
  return useContext(I18nCtx);
}
